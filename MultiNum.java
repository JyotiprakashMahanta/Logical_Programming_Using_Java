import java.util.*;
class MultiNum 
{
	public static void main(String[] args) 
	{
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter two floating number to show multification");
		float first=sc.nextFloat();
		float second=sc.nextFloat();
		float res=first*second;
		System.out.println("the multification is: "+res);
	}
}
